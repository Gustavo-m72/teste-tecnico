Ligaria o primeiro interruptor e iria até a primeira sala, se estivesse ligado OK
Se não estivesse ligado seria uma das outras duas salas
Ligaria o segundo interruptor e iria até a segunda sala, se estivesse ligado seria uma sequencia obvia, mas se estivesse desligado observaria se a lampada estava quente, se estivesse seria a primeira
se não estivesse ligado e a lampada fria então o terceiro liga a segunda, a primeira liga a terceira e a segunda liga a primeira